    <h2>Electrical Safety:</h2>
  <img src="4thsafety.jpg" height="220px" width="420px" >

<ul>
  <li>Never leave appliances such as dishwashers, washing machines or tumble dryers running when you’re out of the house, or asleep.</li>
  <li>Use extension leads carefully. Be aware of the total load on each plug. Never use powerful appliances such as dishwashers, washing machines or tumble dryers on a multi-plug extension lead. Don't use block plug adapters.</li>
  <li>Always switch off electrical items when they’re not in use.</li>
<li>Avoid using candles. Use battery-powered options instead.</li>
<li>If you must use candles, place them on a safe and secure surface well away from anything that could catch fire.Put tea lights in a holder or on a plate. The foil theyre contained in can become extremely hot <br>and melt whats underneath.
</li>
<li>When using chargers only use the charger that came with your device. Charge on a hard, non-flammable surface. Never leave charging devices unattended.</li>
<li>Use heaters safely and never cover them or dry clothes on them. If you are drying clothes, keep them away from the heater.</li>
<li>Only use laptops on a hard surface.</li>








</ul>
