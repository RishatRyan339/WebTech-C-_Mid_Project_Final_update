<h1>Fire safety checklist</h1>
<p>
Use our fire safety checklist to help make you and your family safer at home. We recommend using this checklist in each room of your house to help make sure everything is covered.<p>

    <img src="3rdChecklist.png" height="620px" width="720px"></center>
  <h2> Cooking Safety:</h2>
  <img src="1stsafety.jpg" height="220px" width="420px" >
  <p><li>Keep tea towels and other flammable items away from cookers and hobs.<br></li>
<li>Pull your toaster out from under cupboards before using it and empty the crumb tray after use.<br></li>
<li>Switch off anything you’re not using. <br> </li>
<li>Never put anything metal in the microwave.<br></li>
<li>Always keep an eye on your cooking - never leave anything cooking when you’re out (or asleep).<br></li>
<li>When cooking with oil, use as little as possible. If you are deep-frying,<br> use an electric thermostat-controlled deep-fat fryer rather than deep frying on the hob.<b</li>
<li>Never put water on flames or a smoking pan - it will cause it to explode.<br></li>






</p>
